/**
 * @author lgl
 * @Description: zuuldemo启动类
 * @date 2019/12/28 11:34 下午
 */
@EnableZuulProxy
@SpringBootApplication
public class GatewayApplication {
    public static void main(String[] args) {
        SpringApplication.run(GatewayApplication.class, args);
    }
}
